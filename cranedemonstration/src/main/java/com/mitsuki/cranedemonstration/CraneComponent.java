package com.mitsuki.cranedemonstration;

public class CraneComponent {
    private float x; //初始位置
    private float y; //初始位置

    public CraneComponent() {
        this.x = 0;
        this.y = 0;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
